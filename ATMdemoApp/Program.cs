using System;

public class cardHolder {
    string cardNum;
    int PIN;
    string firstName;
    string lastName;
    double balance;
    public cardHolder(string cardNum, int PIN, string firstName, string lastName, double balance ) {
        this.cardNum = cardNum;
        this.PIN = PIN;
        this.firstName = firstName;
        this.lastName = lastName;
        this.balance = balance;
    }
   
    
    
   
}
